'use strict';

import { Handler } from 'handler/handler';

//******************************************************************************
export class
  Source
extends
  Handler
{
//==============================================================================
constructor(config)
{
  super(config);
  this._pipes = new Set();
}
//==============================================================================
onAdd(pipe)
{
  this._pipes.add(pipe);
}
//==============================================================================
onRemove(pipe)
{
  this._pipes.delete(pipe);
}
//==============================================================================
}//Source
//******************************************************************************
